type ValidMonth =
  | 'jan'
  | 'feb'
  | 'mar'
  | 'apr'
  | 'may'
  | 'jun'
  | 'jul'
  | 'aug'
  | 'sep'
  | 'oct'
  | 'nov'
  | 'dec';

// Helper function to check if a string is a valid month
export const isValidMonth = (month: string): month is ValidMonth => {
  const validMonths: ValidMonth[] = [
    'jan',
    'feb',
    'mar',
    'apr',
    'may',
    'jun',
    'jul',
    'aug',
    'sep',
    'oct',
    'nov',
    'dec',
  ];
  return validMonths.includes(month as ValidMonth);
};
